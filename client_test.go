func TestPacketSize(t *testing.T) {
	cases := []struct {
		in   packet
		want int
	}{
		{packet{0, 0, ""}, 10},
		{packet{math.MaxInt, 0, ""}, 10},
		{packet{1, serverdataAuth, "abcdefghijklmnopqrstuvwxyz"}, 36},
	}
	for _, c := range cases {
		got := c.in.size()
		if got != c.want {
			t.Errorf("Packet %v, expected size %d, got %d", c.in, c.want, got)
		}
	}
}

